    // API
    public function populate(Request $request)
    {
        $data = Department::all();

        if ($q = $request->input('q')) {
            $data = Department::whereRaw("UPPER(TRIM(name)) LIKE UPPER(TRIM('%{$q}%'))", [$q])
                ->get();
        }

        $i = 0;
        $rows = array();

        foreach ($data as $row) {
            $rows[$i]['id'] = $row->id;
            $rows[$i]['text'] = $row->name;
            $i++;
        }

        return [
            'items' => $rows
        ];
    }
